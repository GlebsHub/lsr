function imageUrl(seed: number): string {
  return `${import.meta.env.BASE_URL}${nevaHausImages[(seed - 1) % nevaHausImages.length]}`
}

function apartmentImages(index: number): Pick<Apartment, 'image' | 'gallery'> {
  const offset = (index - 1) * 4

  return {
    image: imageUrl(offset + 1),
    gallery: [imageUrl(offset + 2), imageUrl(offset + 3), imageUrl(offset + 4)],
  }
}

